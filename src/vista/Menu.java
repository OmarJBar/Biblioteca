    /**
     * este metodo validara los campos que no ingrese un caracter invalido en
     * este caso evita los numeros
     *
     * @param evt esta variable recibe un evento como el ingreso de un dato
     */
    public void validarNumero(KeyEvent evt) {
        char valida = evt.getKeyChar();
        if (Character.isDigit(valida)) {
            evt.consume();
            getToolkit().beep();
        }
    }

    /**
     * este metodo validara los campos que no ingrese un caracter invalido en
     * este caso evita las letras
     *
     * @param evt esta variable recibe un evento como el ingreso de un dato
     */
    public void validarLetra(KeyEvent evt) {
        char valida = evt.getKeyChar();
        if (Character.isLetter(valida)) {
            evt.consume();
            getToolkit().beep();
        }
    }

    /**
     * este metodo restringe la longitud de los campos
     *
     * @param evt
     * @param text
     * @param longitud
     */
    public void validarLongitud(KeyEvent evt, JTextField text, int longitud) {
        if (text.getText().length() == longitud) {
            evt.consume();
            getToolkit().beep();
        }
    }

